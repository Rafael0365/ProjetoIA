package projeto;

public class Copo {
	double espessura;
	double forma;
	public Copo(double espessura, double forma) {
		super();
		this.espessura = espessura;
		this.forma = forma;
	}
	public double getEspessura() {
		return espessura;
	}
	public void setEspessura(double espessura) {
		this.espessura = espessura;
	}
	public double getForma() {
		return forma;
	}
	public void setForma(double forma) {
		this.forma = forma;
	}
	
	

}
