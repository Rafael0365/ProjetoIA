package projeto;

public class Amostra {
	private int x1;
	private int x2;
	private int saidaDesejada;
	public Amostra(int x1, int x2,int saidaDesejada) {
		super();
		this.x1 = x1;
		this.x2 = x2;
		this.saidaDesejada = saidaDesejada;
	}
	
	public int getX1() {
		return x1;
	}
	public void setX1(int x1) {
		this.x1 = x1;
	}
	public int getX2() {
		return x2;
	}
	public void setX2(int x2) {
		this.x2 = x2;
	}

	public int getSaidaDesejada() {
		return saidaDesejada;
	}

	public void setSaidaDesejada(int saidaDesejada) {
		this.saidaDesejada = saidaDesejada;
	}
	
	
	
	

}
